/**
 this code block is beig used to practice template literals concept.
this methodology of calling values of variables in to sentences is commonly also done in Unix scripting
 */

//declaring variables
let myName = 'Aakarsh';
let myCity = 'Pune';

//using template literals
console.log(`I am ${myName} and I live in ${myCity}.`);
console.log('hello');

//testing the typeof() function
console.log(typeof(myName));
console.log(typeof(myCity));

//reassigning new values to the above variables
